public class Weeks {
    private String week;
    private String grade;

    public Weeks(String week, String grade) {
        this.week = week;
        this.grade = grade;
    }

    public String getWeek() {
        return week;
    }

    public String getGrade() {
        return grade;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
